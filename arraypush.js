//PUSH
const teste = [1, 2, 3, 4];
console.log(teste);//adiciona mais um valor no último elemento

teste.push(15);
console.log(teste);

//POP
const teste = [1 ,2 ,3 ,4];//remove o último elemento 
console.log(teste);

teste.pop();
teste.pop();

//UNSHIFT
const teste = [1, 2, 3, 4];//adiciona mais um ao inicio do array
console.log(teste);

teste.unshift(19);
console.log(teste);

